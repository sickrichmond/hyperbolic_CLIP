"""
Zero-shot attribution evaluation for Stage-1 (Attribution-CLIP).

Compares each image embedding to a set of static text anchors and picks
the closest one as the predicted attribution label.

Text anchors (no per-image content needed at inference):
  - "Real image"
  - "Synthetic image generated by FLUX"
  - ... one per generator

Usage:
    CUDA_VISIBLE_DEVICES=0 python -m tests.eval_attribution \\
        --checkpoint  attribution_checkpoint_FLUX.pt \\
        --dataset_path /mnt/data3/rtrebiani/iab_dataset \\
        --captions_dir /mnt/data3/rtrebiani/iab_captions \\
        --generators   FLUX real \\
        --semantics    COCO cat dog wild FFHQ celebahq bedroom church classroom ImageNet-1k

To test on a subset only (faster):
    ... --max_per_class 200
"""
import argparse
import warnings

import torch
import torch.nn.functional as F
from torch.utils.data import DataLoader
from transformers import CLIPTokenizer
from tqdm import tqdm

warnings.filterwarnings("ignore", category=UserWarning, module="PIL")

from models.attribution_clip import AttributionCLIP
from data.iab_clip_dataset import IABCLIPDataset

# Static anchors used at inference.
# Keys are the generator names as they appear in the dataset (or "real").
ANCHOR_TEMPLATES = {
    "real": "Real image",
    "FLUX": "Synthetic image generated by FLUX",
    "SDXL": "Synthetic image generated by SDXL",
    "SD3_5": "Synthetic image generated by SD3_5",
    "SD1_5": "Synthetic image generated by SD1_5",
    "dalle3": "Synthetic image generated by DALL-E 3",
    "4o": "Synthetic image generated by GPT-4o",
    "mid-6.0": "Synthetic image generated by Midjourney 6",
}


def parse_args():
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--checkpoint",    required=True)
    p.add_argument("--dataset_path",  required=True)
    p.add_argument("--captions_dir",  required=True)
    p.add_argument("--generators",    nargs="+", default=["FLUX", "real"])
    p.add_argument("--semantics",     nargs="+",
                   default=["COCO", "cat", "dog", "wild", "FFHQ", "celebahq",
                             "bedroom", "church", "classroom", "ImageNet-1k"])
    p.add_argument("--max_per_class", type=int, default=None)
    p.add_argument("--batch_size",    type=int, default=128)
    p.add_argument("--num_workers",   type=int, default=4)
    return p.parse_args()


@torch.no_grad()
def encode_anchors(model: AttributionCLIP, generators: list[str],
                   tokenizer: CLIPTokenizer, device: str) -> dict[str, torch.Tensor]:
    """Returns {generator: normalised_text_embed (1, D)} for anchors in use."""
    anchors = {}
    for gen in generators:
        text = ANCHOR_TEMPLATES.get(gen, f"Synthetic image generated by {gen}")
        tokens = tokenizer(text, return_tensors="pt",
                           padding="max_length", max_length=77, truncation=True)
        emb = model.encode_text(
            tokens["input_ids"].to(device),
            tokens["attention_mask"].to(device),
        )
        anchors[gen] = emb          # (1, D)
    return anchors


def main():
    args = parse_args()
    device = "cuda" if torch.cuda.is_available() else "cpu"

    # ── Load model ────────────────────────────────────────────────────────────
    print(f"Loading checkpoint: {args.checkpoint}")
    ckpt = torch.load(args.checkpoint, map_location=device, weights_only=False)
    model = AttributionCLIP(
        clip_name=ckpt.get("clip_name", "openai/clip-vit-base-patch32"),
        lora_r=ckpt.get("lora_r", 8),
        lora_alpha=ckpt.get("lora_alpha", 16),
    ).to(device)
    model.clip.load_state_dict(ckpt["lora_state"])
    model.eval()

    tokenizer = CLIPTokenizer.from_pretrained(
        ckpt.get("clip_name", "openai/clip-vit-base-patch32")
    )

    # ── Dataset ───────────────────────────────────────────────────────────────
    dataset = IABCLIPDataset(
        root=args.dataset_path,
        captions_dir=args.captions_dir,
        generators=args.generators,
        semantics=args.semantics,
        processor_name=ckpt.get("clip_name", "openai/clip-vit-base-patch32"),
        max_per_class=args.max_per_class,
    )
    loader = DataLoader(dataset, batch_size=args.batch_size, shuffle=False,
                        num_workers=args.num_workers, pin_memory=True)

    # ── Encode anchors ────────────────────────────────────────────────────────
    anchors = encode_anchors(model, args.generators, tokenizer, device)
    anchor_matrix = torch.cat(list(anchors.values()), dim=0)  # (N_gen, D)
    gen_order = list(anchors.keys())

    # ── Extract image embeddings ──────────────────────────────────────────────
    all_img_emb, all_gt, all_semantic = [], [], []
    with torch.no_grad():
        for batch in tqdm(loader, desc="embedding images"):
            pixel = batch["pixel_values"].to(device)
            emb = model.encode_image(pixel)
            all_img_emb.append(emb.cpu())
            all_gt.extend(batch["generator"])
            all_semantic.extend(batch["semantic"])

    img_emb = torch.cat(all_img_emb, dim=0)                   # (N, D)

    # ── Zero-shot classification ──────────────────────────────────────────────
    sims = img_emb @ anchor_matrix.cpu().T                      # (N, N_gen)
    pred_idx = sims.argmax(dim=1)
    pred_labels = [gen_order[i] for i in pred_idx.tolist()]

    # ── Metrics ───────────────────────────────────────────────────────────────
    correct = sum(p == g for p, g in zip(pred_labels, all_gt))
    total = len(all_gt)
    acc = correct / total

    print(f"\n=== Zero-shot attribution: {args.checkpoint} ===")
    print(f"Generators in eval:  {args.generators}")
    print(f"Anchors used:        {gen_order}")
    print(f"Total samples:       {total}")
    print(f"Overall accuracy:    {100*acc:.1f}%  ({correct}/{total})")

    # Per-generator accuracy
    print("\n--- Per-generator accuracy ---")
    by_gen: dict[str, list[bool]] = {}
    for pred, gt in zip(pred_labels, all_gt):
        by_gen.setdefault(gt, []).append(pred == gt)
    for gen, hits in sorted(by_gen.items()):
        a = sum(hits) / len(hits)
        print(f"  {gen:20s}: {100*a:5.1f}%  ({sum(hits)}/{len(hits)})")

    # Per-semantic accuracy
    print("\n--- Per-semantic accuracy ---")
    by_sem: dict[str, list[bool]] = {}
    for pred, gt, sem in zip(pred_labels, all_gt, all_semantic):
        by_sem.setdefault(sem, []).append(pred == gt)
    for sem, hits in sorted(by_sem.items()):
        a = sum(hits) / len(hits)
        print(f"  {sem:15s}: {100*a:5.1f}%  ({sum(hits)}/{len(hits)})")

    # Cosine similarity stats
    print("\n--- Mean cosine similarity to each anchor ---")
    for j, gen in enumerate(gen_order):
        mask_gt = torch.tensor([g == gen for g in all_gt])
        if mask_gt.any():
            mean_sim = sims[mask_gt, j].mean().item()
            print(f"  {gen:20s} images → {gen:20s} anchor: {mean_sim:.4f}")


if __name__ == "__main__":
    main()
