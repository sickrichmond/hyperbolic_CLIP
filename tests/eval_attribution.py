"""
Attribution evaluation for Stage-1 (Attribution-CLIP).

Two evaluation modes:

1. --mode caption  (default, recommended)
   For each image, encodes two competing captions using the image's own content:
     - "Real image of {content}"
     - "Synthetic image generated by FLUX, showing {content}"
   Picks the one with higher cosine similarity. Tests exactly what the model
   was trained to do.

2. --mode anchor
   Compares each image embedding to short static text anchors:
     - "Real image"
     - "Synthetic image generated by FLUX"
   Useful to probe zero-shot generalisation beyond training distribution.

Usage:
    python -m tests.eval_attribution \\
        --checkpoint  $WORK/checkpoints/attribution_FLUX_vitl14.pt \\
        --dataset_path $WORK/iab_dataset \\
        --captions_dir $WORK/hyp_fine_tuning/iab_captions \\
        --generators   FLUX real \\
        --semantics    COCO cat dog wild FFHQ celebahq bedroom church classroom ImageNet-1k

    # anchor mode:
    python -m tests.eval_attribution ... --mode anchor
"""
import argparse
import warnings

import torch
from torch.utils.data import DataLoader
from transformers import CLIPTokenizer
from tqdm import tqdm

warnings.filterwarnings("ignore", category=UserWarning, module="PIL")

from models.attribution_clip import AttributionCLIP
from data.iab_clip_dataset import IABCLIPDataset

ANCHOR_TEMPLATES = {
    "real": "Real image",
    "FLUX": "Synthetic image generated by FLUX",
    "SDXL": "Synthetic image generated by SDXL",
    "SD3_5": "Synthetic image generated by SD3_5",
    "SD1_5": "Synthetic image generated by SD1_5",
    "dalle3": "Synthetic image generated by DALL-E 3",
    "4o": "Synthetic image generated by GPT-4o",
    "mid-6.0": "Synthetic image generated by Midjourney 6",
}


def parse_args():
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--checkpoint",    required=True)
    p.add_argument("--dataset_path",  required=True)
    p.add_argument("--captions_dir",  required=True)
    p.add_argument("--generators",    nargs="+", default=["FLUX", "real"])
    p.add_argument("--semantics",     nargs="+",
                   default=["COCO", "cat", "dog", "wild", "FFHQ", "celebahq",
                             "bedroom", "church", "classroom", "ImageNet-1k"])
    p.add_argument("--mode",          choices=["caption", "anchor"], default="caption")
    p.add_argument("--max_per_class", type=int, default=None)
    p.add_argument("--batch_size",    type=int, default=128)
    p.add_argument("--num_workers",   type=int, default=4)
    return p.parse_args()


def _tokenize(tokenizer, texts: list[str], device: str):
    tok = tokenizer(texts, return_tensors="pt", padding="max_length",
                    truncation=True, max_length=77)
    return tok["input_ids"].to(device), tok["attention_mask"].to(device)


def print_metrics(pred_labels, all_gt, all_semantic, checkpoint, mode):
    correct = sum(p == g for p, g in zip(pred_labels, all_gt))
    total = len(all_gt)

    print(f"\n=== Attribution eval ({mode} mode): {checkpoint} ===")
    print(f"Total samples:    {total}")
    print(f"Overall accuracy: {100*correct/total:.1f}%  ({correct}/{total})")

    print("\n--- Per-generator accuracy ---")
    by_gen: dict[str, list[bool]] = {}
    for pred, gt in zip(pred_labels, all_gt):
        by_gen.setdefault(gt, []).append(pred == gt)
    per_gen_accs = []
    for gen, hits in sorted(by_gen.items()):
        a = sum(hits) / len(hits)
        per_gen_accs.append(a)
        print(f"  {gen:20s}: {100*a:5.1f}%  ({sum(hits)}/{len(hits)})")
    print(f"  {'balanced avg':20s}: {100 * sum(per_gen_accs) / len(per_gen_accs):5.1f}%")

    print("\n--- Per-semantic accuracy ---")
    by_sem: dict[str, list[bool]] = {}
    for pred, gt, sem in zip(pred_labels, all_gt, all_semantic):
        by_sem.setdefault(sem, []).append(pred == gt)
    for sem, hits in sorted(by_sem.items()):
        a = sum(hits) / len(hits)
        print(f"  {sem:15s}: {100*a:5.1f}%  ({sum(hits)}/{len(hits)})")


@torch.no_grad()
def eval_caption_mode(model, loader, tokenizer, generators, device, checkpoint):
    """Per-image: pick higher sim between real-augmented and fake-augmented caption."""
    fake_gen = next(g for g in generators if g != "real")

    all_pred, all_gt, all_semantic = [], [], []
    for batch in tqdm(loader, desc="caption mode"):
        pixel    = batch["pixel_values"].to(device)
        raw_caps = batch["raw_caption"]

        img_emb = model.encode_image(pixel)   # (B, D)

        real_texts = [IABCLIPDataset.CAPTION_REAL.format(caption=c) for c in raw_caps]
        fake_texts = [IABCLIPDataset.CAPTION_FAKE.format(generator=fake_gen, caption=c)
                      for c in raw_caps]

        r_ids, r_mask = _tokenize(tokenizer, real_texts, device)
        f_ids, f_mask = _tokenize(tokenizer, fake_texts, device)

        sim_real = (img_emb * model.encode_text(r_ids, r_mask)).sum(dim=1)  # (B,)
        sim_fake = (img_emb * model.encode_text(f_ids, f_mask)).sum(dim=1)  # (B,)

        preds = ["real" if sr > sf else fake_gen
                 for sr, sf in zip(sim_real.tolist(), sim_fake.tolist())]
        all_pred.extend(preds)
        all_gt.extend(batch["generator"])
        all_semantic.extend(batch["semantic"])

    print_metrics(all_pred, all_gt, all_semantic, checkpoint, "caption")


@torch.no_grad()
def eval_anchor_mode(model, loader, tokenizer, generators, device, checkpoint):
    """Compare image embeddings to short static text anchors."""
    anchors = {}
    for gen in generators:
        text = ANCHOR_TEMPLATES.get(gen, f"Synthetic image generated by {gen}")
        ids, mask = _tokenize(tokenizer, [text], device)
        anchors[gen] = model.encode_text(ids, mask)   # (1, D)

    gen_order = list(anchors.keys())
    anchor_matrix = torch.cat(list(anchors.values()), dim=0).cpu()  # (G, D)

    all_img_emb, all_gt, all_semantic = [], [], []
    for batch in tqdm(loader, desc="anchor mode"):
        emb = model.encode_image(batch["pixel_values"].to(device))
        all_img_emb.append(emb.cpu())
        all_gt.extend(batch["generator"])
        all_semantic.extend(batch["semantic"])

    img_emb = torch.cat(all_img_emb, dim=0)
    sims = img_emb @ anchor_matrix.T
    pred_labels = [gen_order[i] for i in sims.argmax(dim=1).tolist()]

    print_metrics(pred_labels, all_gt, all_semantic, checkpoint, "anchor")

    print("\n--- Mean cosine similarity to each anchor ---")
    for j, gen in enumerate(gen_order):
        mask_gt = torch.tensor([g == gen for g in all_gt])
        if mask_gt.any():
            print(f"  {gen:20s} images → {gen:20s} anchor: {sims[mask_gt, j].mean():.4f}")


def main():
    args = parse_args()
    device = "cuda" if torch.cuda.is_available() else "cpu"

    ckpt = torch.load(args.checkpoint, map_location=device, weights_only=False)
    clip_name = ckpt.get("clip_name", "openai/clip-vit-base-patch32")

    model = AttributionCLIP(
        clip_name=clip_name,
        lora_r=ckpt.get("lora_r", 8),
        lora_alpha=ckpt.get("lora_alpha", 16),
    ).to(device)
    model.clip.load_state_dict(ckpt["lora_state"])
    model.eval()

    tokenizer = CLIPTokenizer.from_pretrained(clip_name)

    dataset = IABCLIPDataset(
        root=args.dataset_path,
        captions_dir=args.captions_dir,
        generators=args.generators,
        semantics=args.semantics,
        processor_name=clip_name,
        max_per_class=args.max_per_class,
    )
    loader = DataLoader(dataset, batch_size=args.batch_size, shuffle=False,
                        num_workers=args.num_workers, pin_memory=True)

    if args.mode == "anchor":
        eval_anchor_mode(model, loader, tokenizer, args.generators, device, args.checkpoint)
    else:
        eval_caption_mode(model, loader, tokenizer, args.generators, device, args.checkpoint)


if __name__ == "__main__":
    main()
