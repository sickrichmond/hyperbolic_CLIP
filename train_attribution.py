"""
Stage-1 attribution fine-tuning with HYPERBOLIC ENTAILMENT CONES.

Each image of class c (e.g. "real" or "FLUX") is pushed inside the entailment
cone of the corresponding fixed text anchor:
  - "A real image"
  - "A synthetic image generated by FLUX"
and outside the cones of all other classes. No content captions are used in
the training objective — only the class anchors. At inference time, an image
is classified by projecting it into Lorentz space and picking the anchor whose
cone it sits in.

Usage:
    CUDA_VISIBLE_DEVICES=0,1,2,3 python train_attribution.py \\
        --dataset_path  $WORK/iab_dataset \\
        --captions_dir  $WORK/hyp_fine_tuning/iab_captions \\
        --generators    real FLUX \\
        --semantics     COCO cat dog wild FFHQ celebahq bedroom church classroom ImageNet-1k \\
        --clip_name     openai/clip-vit-large-patch14 \\
        --output        $WORK/checkpoints/attribution_FLUX_vitl14.pt
"""
import argparse
from pathlib import Path

import torch
import torch.nn as nn
from torch.amp import GradScaler, autocast
from torch.utils.data import DataLoader, WeightedRandomSampler
from tqdm import tqdm
from transformers import CLIPTokenizer

from data.iab_clip_dataset import IABCLIPDataset
from models.attribution_clip import AttributionCLIP
from losses.attribution_loss import EntailmentConeLoss, predict_class


# Class-anchor text templates. Order matters: this defines the integer labels.
def build_anchors(generators: list[str]) -> tuple[list[str], list[str]]:
    """
    Returns (class_names, anchor_texts) in a fixed order.
    class_names[i] is the generator string ("real", "FLUX", ...) used in the dataset;
    anchor_texts[i] is the natural-language anchor encoded by CLIP.
    """
    ordered: list[str] = []
    if "real" in generators:
        ordered.append("real")
    for g in generators:
        if g != "real":
            ordered.append(g)
    anchor_texts = []
    for c in ordered:
        if c == "real":
            anchor_texts.append("A real image")
        else:
            anchor_texts.append(f"A synthetic image generated by {c}")
    return ordered, anchor_texts


def parse_args():
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--dataset_path",   required=True)
    p.add_argument("--captions_dir",   required=True)
    p.add_argument("--generators",     nargs="+", default=["real", "FLUX"])
    p.add_argument("--semantics",      nargs="+",
                   default=["COCO", "cat", "dog", "wild", "FFHQ", "celebahq",
                            "bedroom", "church", "classroom", "ImageNet-1k"])
    p.add_argument("--output",         default="attribution_checkpoint_FLUX.pt")
    p.add_argument("--clip_name",      default="openai/clip-vit-base-patch32")
    p.add_argument("--lora_r",         type=int,   default=8)
    p.add_argument("--lora_alpha",     type=int,   default=16)
    p.add_argument("--hyperbolic_dim", type=int,   default=128)
    p.add_argument("--curv",           type=float, default=1.0)
    p.add_argument("--min_radius",     type=float, default=0.1)
    p.add_argument("--margin",         type=float, default=0.1)
    p.add_argument("--lambda_neg",     type=float, default=1.0)
    p.add_argument("--lambda_norm",    type=float, default=0.0,
                   help="Weight of the anchor-norm regulariser (0 disables it).")
    p.add_argument("--target_norm",    type=float, default=0.0,
                   help="Target ‖t_anchor‖. With curv=1, ψ ≈ π/8 at ‖t‖≈2.6, π/16 at ‖t‖≈5.")
    p.add_argument("--batch_size",     type=int,   default=256)
    p.add_argument("--num_epochs",     type=int,   default=10)
    p.add_argument("--lr",             type=float, default=5e-5)
    p.add_argument("--weight_decay",   type=float, default=0.01)
    p.add_argument("--val_frac",       type=float, default=0.2)
    p.add_argument("--seed",           type=int,   default=42)
    p.add_argument("--max_per_class",  type=int,   default=None)
    p.add_argument("--num_workers",    type=int,   default=8)
    return p.parse_args()


def make_balanced_sampler(dataset: IABCLIPDataset) -> WeightedRandomSampler:
    is_real = [g == "real" for _, g, _ in dataset.samples]
    n_real = sum(is_real)
    n_fake = len(is_real) - n_real
    if n_real == 0 or n_fake == 0:
        raise RuntimeError(f"Dataset needs both real and fake: {n_real} real, {n_fake} fake")
    weights = [1.0 / n_real if r else 1.0 / n_fake for r in is_real]
    return WeightedRandomSampler(weights, num_samples=len(dataset), replacement=True)


@torch.no_grad()
def encode_anchors(model, anchor_texts: list[str], tokenizer, device: str) -> torch.Tensor:
    """Encode the fixed text anchors and return their Lorentz-space coords (K, D)."""
    tok = tokenizer(anchor_texts, return_tensors="pt", padding="max_length",
                    truncation=True, max_length=77)
    x_anc, _ = model.encode_text(tok["input_ids"].to(device),
                                 tok["attention_mask"].to(device))
    return x_anc


@torch.no_grad()
def run_validation(model_inner, val_loader, anchor_texts, tokenizer, class_names,
                   device, curv) -> dict:
    model_inner.eval()

    # Anchors must be re-encoded under the current text-encoder weights
    tok = tokenizer(anchor_texts, return_tensors="pt", padding="max_length",
                    truncation=True, max_length=77)
    x_anc, _ = model_inner.encode_text(tok["input_ids"].to(device),
                                       tok["attention_mask"].to(device))

    per_class_correct = {c: 0 for c in class_names}
    per_class_total   = {c: 0 for c in class_names}

    for batch in tqdm(val_loader, desc="val", leave=False):
        pixel = batch["pixel_values"].to(device)
        with autocast("cuda"):
            x_img, _ = model_inner.encode_image(pixel)
        pred_idx = predict_class(x_img, x_anc, curv=curv)
        pred_names = [class_names[i] for i in pred_idx.tolist()]
        for pred, gt in zip(pred_names, batch["generator"]):
            per_class_total[gt] += 1
            per_class_correct[gt] += int(pred == gt)

    total = sum(per_class_total.values())
    correct = sum(per_class_correct.values())
    per_class_acc = {
        c: (per_class_correct[c] / per_class_total[c]) if per_class_total[c] > 0 else 0.0
        for c in class_names
    }
    balanced = sum(per_class_acc.values()) / len(class_names)
    return {
        "overall_acc":   correct / total if total else 0.0,
        "balanced_acc":  balanced,
        "per_class_acc": per_class_acc,
        "total":         total,
    }


def main():
    args = parse_args()
    device = "cuda" if torch.cuda.is_available() else "cpu"

    class_names, anchor_texts = build_anchors(args.generators)
    name_to_idx = {n: i for i, n in enumerate(class_names)}
    print(f"Class anchors:")
    for i, (c, t) in enumerate(zip(class_names, anchor_texts)):
        print(f"  [{i}] {c:8s} → \"{t}\"")

    # ── Datasets ──────────────────────────────────────────────────────────────
    print("\n=== Train split ===")
    train_ds = IABCLIPDataset(
        root=args.dataset_path,
        captions_dir=args.captions_dir,
        generators=args.generators,
        semantics=args.semantics,
        processor_name=args.clip_name,
        max_per_class=args.max_per_class,
        split="train",
        val_frac=args.val_frac,
        seed=args.seed,
    )
    print("\n=== Val split ===")
    val_ds = IABCLIPDataset(
        root=args.dataset_path,
        captions_dir=args.captions_dir,
        generators=args.generators,
        semantics=args.semantics,
        processor_name=args.clip_name,
        max_per_class=args.max_per_class,
        split="val",
        val_frac=args.val_frac,
        seed=args.seed,
    )

    sampler = make_balanced_sampler(train_ds)
    train_loader = DataLoader(
        train_ds, batch_size=args.batch_size, sampler=sampler,
        num_workers=args.num_workers, pin_memory=True, drop_last=True,
    )
    val_loader = DataLoader(
        val_ds, batch_size=args.batch_size, shuffle=False,
        num_workers=args.num_workers, pin_memory=True,
    )

    # ── Model ─────────────────────────────────────────────────────────────────
    model = AttributionCLIP(
        clip_name=args.clip_name,
        lora_r=args.lora_r,
        lora_alpha=args.lora_alpha,
        hyperbolic_dim=args.hyperbolic_dim,
        curv=args.curv,
    ).to(device)

    if torch.cuda.device_count() > 1:
        print(f"Using {torch.cuda.device_count()} GPUs")
        model = nn.DataParallel(model)

    core = model.module if isinstance(model, nn.DataParallel) else model
    core.print_trainable_summary()

    optimizer = torch.optim.AdamW(
        core.trainable_parameters(),
        lr=args.lr,
        weight_decay=args.weight_decay,
    )
    steps_per_epoch = len(train_loader)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(
        optimizer, T_max=args.num_epochs * steps_per_epoch, eta_min=1e-6
    )
    scaler = GradScaler("cuda")
    cone_loss = EntailmentConeLoss(
        curv=args.curv, min_radius=args.min_radius,
        margin=args.margin, lambda_neg=args.lambda_neg,
        lambda_norm=args.lambda_norm, target_norm=args.target_norm,
    )

    # Tokenized anchors stay constant; only the text-encoder weights change.
    tokenizer = CLIPTokenizer.from_pretrained(args.clip_name)
    anchor_tok = tokenizer(anchor_texts, return_tensors="pt", padding="max_length",
                           truncation=True, max_length=77)
    anchor_ids  = anchor_tok["input_ids"].to(device)
    anchor_mask = anchor_tok["attention_mask"].to(device)

    best_balanced = -1.0
    out_path = Path(args.output)
    out_path.parent.mkdir(parents=True, exist_ok=True)

    # ── Training loop ─────────────────────────────────────────────────────────
    for epoch in range(1, args.num_epochs + 1):
        model.train()
        sums = {"loss": 0.0, "loss_pos": 0.0, "loss_neg": 0.0, "loss_norm": 0.0,
                "cone_acc": 0.0, "inside_pos": 0.0,
                "mean_psi": 0.0, "mean_xi_pos": 0.0, "mean_anc_norm": 0.0}
        bar = tqdm(train_loader, desc=f"Epoch {epoch}/{args.num_epochs}")
        for step, batch in enumerate(bar, 1):
            pixel = batch["pixel_values"].to(device)
            labels = torch.tensor([name_to_idx[g] for g in batch["generator"]],
                                  device=device, dtype=torch.long)

            with autocast("cuda"):
                x_img = model(pixel)                             # DataParallel splits batch across GPUs
                x_anc, _ = core.encode_text(anchor_ids, anchor_mask)  # K=2, fast, primary GPU
            loss, stats = cone_loss(x_img, x_anc, labels)

            optimizer.zero_grad()
            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            nn.utils.clip_grad_norm_(core.trainable_parameters(), 1.0)
            scaler.step(optimizer)
            scaler.update()
            scheduler.step()

            sums["loss"]        += loss.item()
            for k in ("loss_pos", "loss_neg", "loss_norm", "cone_acc", "inside_pos",
                      "mean_psi", "mean_xi_pos", "mean_anc_norm"):
                sums[k] += stats[k].item()
            if step % 25 == 0 or step == steps_per_epoch:
                bar.set_postfix(
                    loss=f"{sums['loss']/step:.3f}",
                    pos=f"{sums['loss_pos']/step:.3f}",
                    neg=f"{sums['loss_neg']/step:.3f}",
                    norm=f"{sums['loss_norm']/step:.3f}",
                    acc=f"{sums['cone_acc']/step:.3f}",
                    psi=f"{sums['mean_psi']/step:.3f}",
                    anc=f"{sums['mean_anc_norm']/step:.2f}",
                )

        avg = {k: v / steps_per_epoch for k, v in sums.items()}
        print(f"\nEpoch {epoch}: train loss={avg['loss']:.4f} "
              f"(pos={avg['loss_pos']:.4f} neg={avg['loss_neg']:.4f} "
              f"norm={avg['loss_norm']:.4f}) "
              f"train cone_acc={100*avg['cone_acc']:.1f}%  "
              f"inside={100*avg['inside_pos']:.1f}%  "
              f"ψ̄={avg['mean_psi']:.3f}  ξ̄_pos={avg['mean_xi_pos']:.3f}  "
              f"‖t̄‖={avg['mean_anc_norm']:.2f}  "
              f"lr={scheduler.get_last_lr()[0]:.2e}")

        # ── Validation ───────────────────────────────────────────────────────
        val = run_validation(core, val_loader, anchor_texts, tokenizer,
                             class_names, device, args.curv)
        print(f"  val: overall={100*val['overall_acc']:.1f}%  "
              f"balanced={100*val['balanced_acc']:.1f}%  ({val['total']} samples)")
        for c, a in val["per_class_acc"].items():
            print(f"    {c:10s}: {100*a:5.1f}%")

        # ── Save best checkpoint by balanced val accuracy ────────────────────
        if val["balanced_acc"] > best_balanced:
            best_balanced = val["balanced_acc"]
            torch.save(
                {
                    "lora_state":      core.clip.state_dict(),
                    "projection":      core.projection.state_dict(),
                    "clip_name":       args.clip_name,
                    "lora_r":          args.lora_r,
                    "lora_alpha":      args.lora_alpha,
                    "hyperbolic_dim":  args.hyperbolic_dim,
                    "curv":            args.curv,
                    "min_radius":      args.min_radius,
                    "class_names":     class_names,
                    "anchor_texts":    anchor_texts,
                    "generators":      args.generators,
                    "semantics":       args.semantics,
                    "val_balanced":    val["balanced_acc"],
                    "epoch":           epoch,
                },
                out_path,
            )
            print(f"  ↳ saved checkpoint (balanced val={100*best_balanced:.1f}%) → {out_path}")

    print(f"\nBest balanced val accuracy: {100*best_balanced:.1f}%  ({out_path})")


if __name__ == "__main__":
    main()
