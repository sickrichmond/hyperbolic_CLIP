"""
Dataset for attribution-aware CLIP fine-tuning on ImageAttributionBench.

Each sample is an (image, augmented_caption) pair where the caption encodes
both the visual content and the attribution label:
  - real:  "Real image of {caption}"
  - fake:  "Synthetic image generated by {generator}, showing {caption}"

Caption CSVs come from:
    scripts/download_iab_captions.py  →  iab_captions/

IAB image layout (from scripts/download_iab.py):
    iab_dataset/
      real/
        COCO/                   ← flat semantics
        ImageNet-1k/
        AnimalFace/cat/         ← hierarchical semantics
        ...
      FLUX/
        (same structure)

Filename conventions
--------------------
Real images:      sequential stems, e.g. 000000.jpg
                  → looked up by stem in the per-semantic CSV

Synthetic images: {csv_prefix}_p{N}_i{K}.png  (N = prompt row, K = variant 0/1)
                  e.g. COCO-new_p42_i0.png, AnimalFace_cat_p7_i1.png
                  → looked up by row index N in the matching CSV
"""
import re
from pathlib import Path
from typing import Optional

import pandas as pd
import torch
from torch.utils.data import Dataset
from PIL import Image
from transformers import CLIPImageProcessor, CLIPTokenizer

# ── Real-image lookup: semantic key → (CSV file, caption column) ──────────────
_REAL_CSV: dict[str, tuple[str, int]] = {
    "COCO":        ("COCO.csv",                 1),
    "cat":         ("AnimalFace_cat.csv",        1),
    "dog":         ("AnimalFace_dog.csv",        1),
    "wild":        ("AnimalFace_wild.csv",       1),
    "FFHQ":        ("HumanFace_FFHQ.csv",        1),
    "celebahq":    ("HumanFace_celebahq.csv",    1),
    "bedroom":     ("Scene_LSUN_bedroom.csv",    1),
    "church":      ("Scene_LSUN_church.csv",     1),
    "classroom":   ("Scene_LSUN_classroom.csv",  1),
    "ImageNet-1k": ("imagenet-1k.csv",           2),  # ImgPath, Label, Caption
}

# ── Synthetic-image lookup: filename prefix → (CSV file, caption column) ─────
# Synthetic filenames: {prefix}_p{N}_i{K}.png  — row N in the CSV.
_FAKE_PREFIX_CSV: dict[str, tuple[str, int]] = {
    "COCO-new":             ("COCO-new.csv",             1),
    "AnimalFace_cat":       ("AnimalFace_cat.csv",        1),
    "AnimalFace_dog":       ("AnimalFace_dog.csv",        1),
    "AnimalFace_wild":      ("AnimalFace_wild.csv",       1),
    "HumanFace_FFHQ":       ("HumanFace_FFHQ.csv",        1),
    "HumanFace_celebahq":   ("HumanFace_celebahq.csv",    1),
    "Scene_LSUN_bedroom":   ("Scene_LSUN_bedroom.csv",    1),
    "Scene_LSUN_church":    ("Scene_LSUN_church.csv",     1),
    "Scene_LSUN_classroom": ("Scene_LSUN_classroom.csv",  1),
    "imagenet-1k-new":      ("imagenet-1k-new.csv",       2),
}

# ── IAB directory layout ──────────────────────────────────────────────────────
_SEMANTIC_DIR: dict[str, tuple[Optional[str], str]] = {
    "COCO":        (None,         "COCO"),
    "ImageNet-1k": (None,         "ImageNet-1k"),
    "cat":         ("AnimalFace", "cat"),
    "dog":         ("AnimalFace", "dog"),
    "wild":        ("AnimalFace", "wild"),
    "FFHQ":        ("HumanFace",  "FFHQ"),
    "celebahq":    ("HumanFace",  "celebahq"),
    "bedroom":     ("Scene",      "bedroom"),
    "church":      ("Scene",      "church"),
    "classroom":   ("Scene",      "classroom"),
}

_IMAGE_EXTS = {".jpg", ".jpeg", ".png", ".JPEG"}
_FAKE_STEM_RE = re.compile(r"^(.+)_p(\d+)_i\d+$")


# ── CSV loaders ───────────────────────────────────────────────────────────────

def _load_by_stem(captions_dir: Path, csv_name: str, cap_col: int) -> dict[str, str]:
    """Returns {img_stem: caption}. Used for real images."""
    path = captions_dir / csv_name
    if not path.exists():
        return {}
    df = pd.read_csv(path, header=0)
    return {
        Path(row[0]).stem: (str(row[cap_col]) if len(row) > cap_col else "")
        for row in df.itertuples(index=False)
    }


def _load_by_index(captions_dir: Path, csv_name: str, cap_col: int) -> list[str]:
    """Returns captions indexed by row. Used for synthetic images."""
    path = captions_dir / csv_name
    if not path.exists():
        return []
    df = pd.read_csv(path, header=0)
    return [
        str(row[cap_col]) if len(row) > cap_col else ""
        for row in df.itertuples(index=False)
    ]


# ── Image directory helper ────────────────────────────────────────────────────

def _img_dir(root: Path, generator: str, semantic: str) -> Path:
    super_cat, subdir = _SEMANTIC_DIR[semantic]
    if super_cat is None:
        return root / generator / subdir
    return root / generator / super_cat / subdir


# ── Caption lookup ────────────────────────────────────────────────────────────

def _lookup_real(by_stem: dict[str, str], img_path: Path) -> str:
    return by_stem.get(img_path.stem, "")


def _lookup_fake(by_index: dict[str, list[str]], img_path: Path) -> str:
    m = _FAKE_STEM_RE.match(img_path.stem)
    if not m:
        return ""
    prefix, n = m.group(1), int(m.group(2))
    caps = by_index.get(prefix, [])
    return caps[n] if n < len(caps) else ""


# ── Dataset ───────────────────────────────────────────────────────────────────

class IABCLIPDataset(Dataset):
    """
    (image, attribution-augmented caption) pairs for CLIP fine-tuning.

    Args:
        root:           Root of extracted IAB dataset.
        captions_dir:   Directory with downloaded caption CSV files.
        generators:     Generator subdirs to include, e.g. ["FLUX", "real"].
        semantics:      Semantic classes to include, e.g. ["COCO", "cat"].
        max_per_class:  Cap images per (generator, semantic) pair.
    """

    CAPTION_REAL = "Real image of {caption}"
    CAPTION_FAKE = "Synthetic image generated by {generator}, showing {caption}"

    def __init__(
        self,
        root: str,
        captions_dir: str,
        generators: list[str],
        semantics: list[str],
        processor_name: str = "openai/clip-vit-base-patch32",
        max_per_class: Optional[int] = None,
        caption_real: str = CAPTION_REAL,
        caption_fake: str = CAPTION_FAKE,
        split: str = "all",
        val_frac: float = 0.2,
        seed: int = 42,
        include_uncaptioned: bool = False,
    ):
        """
        split: "train" | "val" | "all"
            - "all":   every captioned sample (plus uncaptioned if include_uncaptioned)
            - "train": 1 - val_frac of CAPTIONED samples per (generator, semantic)
            - "val":   val_frac of captioned samples per class, plus ALL
                       uncaptioned samples of that class when include_uncaptioned=True
                       (these never appear in train since the hierarchical loss
                       needs a caption — using them only at eval is loss-free).

        Deterministic shuffle ensures the same split across runs.
        """
        if split not in {"train", "val", "all"}:
            raise ValueError(f"split must be train/val/all, got {split!r}")
        self.caption_real = caption_real
        self.caption_fake = caption_fake
        self.split = split
        self.val_frac = val_frac
        self.seed = seed
        self.include_uncaptioned = include_uncaptioned
        caps_p = Path(captions_dir)
        root_p = Path(root)

        # Real images: by-stem index per semantic class
        self._real_idx: dict[str, dict[str, str]] = {}
        for sem in semantics:
            if sem in _REAL_CSV:
                csv_name, cap_col = _REAL_CSV[sem]
                idx = _load_by_stem(caps_p, csv_name, cap_col)
                self._real_idx[sem] = idx
                status = f"{len(idx):6d} captions" if idx else "NOT FOUND"
                print(f"  [real/{sem:12s}] {status}  ← {csv_name}")

        # Synthetic images: by-index lists keyed by filename prefix
        all_prefixes = set(_FAKE_PREFIX_CSV.keys())
        self._fake_idx: dict[str, list[str]] = {}
        loaded_csvs: set[str] = set()
        for prefix, (csv_name, cap_col) in _FAKE_PREFIX_CSV.items():
            if csv_name not in loaded_csvs:
                caps = _load_by_index(caps_p, csv_name, cap_col)
                status = f"{len(caps):6d} captions" if caps else "NOT FOUND"
                print(f"  [fake prefix {prefix:22s}] {status}  ← {csv_name}")
                loaded_csvs.add(csv_name)
            self._fake_idx[prefix] = _load_by_index(caps_p, csv_name, cap_col)

        # Walk images, partition by caption presence, deterministically split
        # captioned ones into train/val. Uncaptioned (almost always real images
        # whose Qwen caption wasn't generated) optionally augment the val set.
        import random
        self.samples: list[tuple[Path, str, str]] = []
        n_dropped = 0
        n_uncap_in_val = 0
        for gen in generators:
            for sem in semantics:
                img_dir = _img_dir(root_p, gen, sem)
                if not img_dir.exists():
                    print(f"  [IABCLIPDataset] not found, skipping: {img_dir}")
                    continue
                imgs = sorted(p for p in img_dir.iterdir()
                               if p.suffix in _IMAGE_EXTS)
                captioned, uncaptioned = [], []
                for p in imgs:
                    if self._get_raw_caption_static(p, gen, sem):
                        captioned.append(p)
                    else:
                        uncaptioned.append(p)

                rng = random.Random(f"{seed}:{gen}:{sem}")
                rng.shuffle(captioned)
                n_val = int(round(len(captioned) * val_frac))
                if split == "train":
                    chosen = captioned[n_val:]
                elif split == "val":
                    chosen = captioned[:n_val]
                    if include_uncaptioned:
                        chosen = chosen + uncaptioned
                        n_uncap_in_val += len(uncaptioned)
                    else:
                        n_dropped += len(uncaptioned)
                else:  # "all"
                    chosen = captioned + (uncaptioned if include_uncaptioned else [])
                    if not include_uncaptioned:
                        n_dropped += len(uncaptioned)
                # For train/all, uncaptioned that we DID NOT include count as dropped.
                if split == "train":
                    n_dropped += len(uncaptioned)
                if max_per_class is not None:
                    chosen = chosen[:max_per_class]
                for p in chosen:
                    self.samples.append((p, gen, sem))

        self.processor = CLIPImageProcessor.from_pretrained(processor_name)
        self.tokenizer = CLIPTokenizer.from_pretrained(processor_name)

        n_real = sum(1 for _, g, _ in self.samples if g == "real")
        n_fake = len(self.samples) - n_real
        msg = f"\nIABCLIPDataset[{split}]: {len(self.samples)} samples ({n_real} real, {n_fake} fake)"
        if n_uncap_in_val:
            msg += f" — incl. {n_uncap_in_val} uncaptioned (eval-only)"
        if n_dropped:
            msg += f" — dropped {n_dropped} without caption"
        print(msg)

    def _get_raw_caption_static(self, img_path: Path, generator: str, semantic: str) -> str:
        """Caption lookup usable during __init__ before self.samples is complete."""
        if generator == "real":
            return _lookup_real(self._real_idx.get(semantic, {}), img_path)
        return _lookup_fake(self._fake_idx, img_path)

    def _get_raw_caption(self, img_path: Path, generator: str, semantic: str) -> str:
        return self._get_raw_caption_static(img_path, generator, semantic)

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> dict:
        img_path, generator, semantic = self.samples[idx]
        is_real = generator == "real"

        img = Image.open(img_path).convert("RGB")

        # grok3 images carry a visible watermark in the bottom-right corner.
        # Matching the IAB benchmark, crop it out (100 px wide × 50 px tall)
        # so the model cannot attribute grok3 by the watermark alone.
        if generator == "grok3":
            w, h = img.size
            if w > 100 and h > 50:
                img = img.crop((0, 0, w - 100, h - 50))

        pixel = self.processor(images=img, return_tensors="pt")["pixel_values"][0]

        raw_cap = self._get_raw_caption(img_path, generator, semantic)
        if is_real:
            text = self.caption_real.format(caption=raw_cap)
        else:
            text = self.caption_fake.format(generator=generator, caption=raw_cap)

        tokens = self.tokenizer(
            text,
            return_tensors="pt",
            padding="max_length",
            truncation=True,
            max_length=77,
        )

        return {
            "pixel_values":   pixel,
            "input_ids":      tokens["input_ids"][0],
            "attention_mask": tokens["attention_mask"][0],
            "is_real":        torch.tensor(is_real, dtype=torch.bool),
            "generator":      generator,
            "semantic":       semantic,
            "raw_caption":    raw_cap,
        }
